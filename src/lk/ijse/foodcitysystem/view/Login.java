    private void lblLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblLoginMouseClicked
        
        String userName=(String) cmbUserName.getSelectedItem();
        String password=new String(pwfPassword.getPassword());
         
        if("Owner".equals(userName)&&"warnasiri".equals(password)){
            BackOffice backOffice=new BackOffice();
            backOffice.setVisible(true);
        }else{
            if("Cashier".equals(userName)&&"tharuka".equals(password)){
                PlaceOrder placeorder=new PlaceOrder();
                placeorder.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this, "Invalid Password");
            }
        }
    }//GEN-LAST:event_lblLoginMouseClicked

