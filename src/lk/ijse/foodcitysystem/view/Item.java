    private void loadAllCategoryNames() {
        try {
            ArrayList<ItemCategoryDTO>categoryNamesList=ItemCategoryController.getAllCategories();
            for(ItemCategoryDTO category:categoryNamesList){
                cmbCategoryName.addItem(category.getCategoryName());
            }
        } catch (Exception ex) {
            Logger.getLogger(Item.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadAllItems() {
        try {
            ArrayList<ItemDTO>allItems=ItemController.getAllItems();
            DefaultTableModel dtm=(DefaultTableModel)tblItems.getModel();
            dtm.setRowCount(0);
            
            if(allItems!=null){
                for(ItemDTO item:allItems){
                    
                    Object[] rowData={
                        item.getItemCode(),
                        item.getDescription(),
                        item.getCategoryName(),
                        item.getCategoryCode()
                        
                    };
                    dtm.addRow(rowData);
                    
                }
            }   } catch (Exception ex) {
            Logger.getLogger(Item.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
