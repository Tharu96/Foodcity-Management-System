    private void lblSave1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSave1MouseClicked
         ItemDTO item=new ItemDTO(txtItemCode.getText(),
                    txtDescription.getText(),
                    (String)cmbCategoryName.getSelectedItem(),
                    txtCategoryCode2.getText()
            );
         DefaultTableModel dtm=(DefaultTableModel)tblItems.getModel();
         
         Object[] rowdata={
             item.getItemCode(),
             item.getDescription(),
             item.getCategoryName(),
             item.getCategoryCode()
             
         };
            
        try{    
            boolean result=ItemController.addItem(item);
            if(result){
                dtm.addRow(rowdata);
                
                JOptionPane.showMessageDialog(this, "Category has been successfully added.");
            }else{
                JOptionPane.showMessageDialog(this, "Category hasn't been added due to some error");
            }
        } catch (Exception ex) {
            Logger.getLogger(Item.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         
       
                                       

   
    }//GEN-LAST:event_lblSave1MouseClicked

