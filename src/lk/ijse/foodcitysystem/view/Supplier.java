    private void loadAllSuppliers() {
        try {
            ArrayList<SupplierDTO>supplierIdList=SupplierController.getAllSuppliers();
            for(SupplierDTO supplierId:supplierIdList){
                cmbSupplier.addItem(supplierId.getSupId());
            }
        } catch (Exception ex) {
            Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadAllSupplierIds() {
        try {
            ArrayList<SupplierDTO>allSuppliers=SupplierController.getAllSuppliers();
            DefaultTableModel dtm=(DefaultTableModel)tblSupplier.getModel();
            dtm.setRowCount(0);
            
            if(allSuppliers!=null){
                for(SupplierDTO supplier:allSuppliers){
                    
                    Object[] rowData={
                        supplier.getSupId(),
                        supplier.getSupName(),
                        supplier.getCompany(),
                        supplier.getAddress(),
                        supplier.getContact()
                        
                    };
                    dtm.addRow(rowData);
                    
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
