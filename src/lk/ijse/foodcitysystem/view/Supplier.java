    private void lblSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSaveMouseClicked
        SupplierDTO supplier=new SupplierDTO(txtSupplierId.getText(),
            txtSupplierName.getText(),
            txtCompany.getText(),
            txtAddress.getText(),
            Integer.parseInt(txtContact.getText())
        );
         DefaultTableModel dtm=(DefaultTableModel)tblSupplier.getModel();
         
         Object[] rowdata={
             supplier.getSupId(),
             supplier.getSupName(),
             supplier.getCompany(),
             supplier.getAddress(),
             supplier.getContact()
            
             
         };
        try {
            boolean result=SupplierController.addSupplier(supplier);
            if(result){
                cmbSupplier.addItem(txtSupplierId.getText());
                dtm.addRow(rowdata);
                JOptionPane.showMessageDialog(this, "Supplier has been successfully added.");
            }else{
                JOptionPane.showMessageDialog(this, "Supplier hasn't been added due to some error");
            }
        } catch (Exception ex) {
            Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblSaveMouseClicked

