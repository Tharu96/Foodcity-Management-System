    public void setValues(GRNDetailDTO gRNDetailDTO) {
        if(gRNDetailDTO==null){return;}
       txtItemCode.setText(gRNDetailDTO.getItemCode());
       txtDesciption.setText(gRNDetailDTO.getDescription());
       txtQtyOnHand.setText(Integer.toString(gRNDetailDTO.getOrderQty()));
       txtCostPrice.setText(Double.toString(gRNDetailDTO.getCostPrice()));
       txtSellingPrice.setText(Double.toString(gRNDetailDTO.getSellingPrice()));
    }
